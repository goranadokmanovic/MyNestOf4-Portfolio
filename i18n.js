(function () {
  const STORAGE_KEY = "mynestof4-lang";
  let currentLang = localStorage.getItem(STORAGE_KEY) || "sr";

  function applyLanguage(lang) {
    currentLang = lang;
    localStorage.setItem(STORAGE_KEY, lang);
    document.documentElement.lang = lang;

    document.querySelectorAll("[data-i18n]").forEach((el) => {
      const key = el.getAttribute("data-i18n");
      if (translations[lang] && translations[lang][key]) {
        el.textContent = translations[lang][key];
      }
    });

    document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
      const key = el.getAttribute("data-i18n-placeholder");
      if (translations[lang] && translations[lang][key]) {
        el.placeholder = translations[lang][key];
      }
    });

    document.querySelectorAll(".lang-option").forEach((opt) => {
      opt.classList.toggle("lang-option--active", opt.dataset.lang === lang);
    });
  }

  const toggle = document.getElementById("lang-toggle");
  if (toggle) {
    toggle.addEventListener("click", (e) => {
      const clicked = e.target.closest("[data-lang]");
      if (clicked && clicked.dataset.lang !== currentLang) {
        applyLanguage(clicked.dataset.lang);
      } else {
        applyLanguage(currentLang === "sr" ? "en" : "sr");
      }
    });
  }

  applyLanguage(currentLang);
})();
